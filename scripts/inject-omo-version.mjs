#!/usr/bin/env node
// Fetches the current upstream oh-my-openagent version and writes it to src/lib/version.ts.
// Strategy: try GitHub raw package.json over HTTPS; on failure, keep existing version.ts.

import { writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, '..', 'src', 'lib', 'version.ts');
const RAW_URL =
  'https://raw.githubusercontent.com/code-yeongyu/oh-my-openagent/dev/package.json';

async function fetchUpstreamVersion() {
  const res = await fetch(RAW_URL, { signal: AbortSignal.timeout(5000) });
  if (!res.ok) throw new Error(`HTTP ${res.status}`);
  const pkg = await res.json();
  if (!pkg.version || typeof pkg.version !== 'string') {
    throw new Error('package.json missing version field');
  }
  return pkg.version;
}

function writeVersionFile(version) {
  const content = `// Auto-generated by scripts/inject-omo-version.mjs. Do not edit by hand.\nexport const OMO_VERSION = ${JSON.stringify(version)};\n`;
  writeFileSync(outPath, content, 'utf8');
}

try {
  const v = await fetchUpstreamVersion();
  writeVersionFile(v);
  console.log(`[inject-omo-version] OMO_VERSION=${v}`);
} catch (err) {
  if (existsSync(outPath)) {
    console.warn(
      `[inject-omo-version] fetch failed (${err.message}); keeping existing version.ts`,
    );
  } else {
    // No upstream and no committed version: fail loudly rather than write a
    // stale literal that would silently downgrade the sidebar.
    console.error(
      `[inject-omo-version] fetch failed (${err.message}) and no existing ${outPath}.`,
    );
    process.exit(1);
  }
}
